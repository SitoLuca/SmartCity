package org.smartcity.smartcity.enums;

/**
 * Rappresenta lo stato di una centralina nel contesto di una Smart City.
 *
 * I possibili valori sono:
 * <ul>
 *     <li><b>online:</b> Indica che la centralina è attiva e in funzione.</li>
 *     <li><b>offline:</b> Indica che la centralina non è attiva o non è connessa.</li>
 * </ul>
 */
public enum Status {
    online, offline
}
